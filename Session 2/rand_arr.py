import numpy as np

list_1 = np.random.randint(0, 10, size = 10)


print("Array: ", list_1)
print("Mean: ", np.mean(list_1))
print("Max: ", np.max(list_1))
print("Min: ", np.min(list_1))
print("Sort: ", np.sort(list_1))
print("Shuffle: ", np.random.permutation(list_1))
print("Root: ", np.sqrt(list_1))



